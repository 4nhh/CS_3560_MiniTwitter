    private void postTweetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_postTweetButtonActionPerformed
        String tweetMessage = postTweetContent.getText();
        String formattedTweet = admin.postTweet(userID, tweetMessage);
        postTweetContent.setText("");
        updateFeed(formattedTweet);
        updateFollowerFeeds(formattedTweet);
        admin.updateStatByType("Tweets");
        admin.updateStatByType("Positivity");
    }//GEN-LAST:event_postTweetButtonActionPerformed

    private void followButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_followButtonActionPerformed
        String userToFollow = followField.getText().replaceAll("[^a-zA-Z0-9]", "");
        if (admin.getUserFromID(userToFollow) != null && !userToFollow.equals(userID)) {
            admin.followUser(userID, userToFollow);
            followField.setText("");
            updateFollowingList(userToFollow);
        }
    }//GEN-LAST:event_followButtonActionPerformed

