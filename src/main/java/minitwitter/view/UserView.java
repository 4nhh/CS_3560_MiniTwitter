    public void populateFeed() {
        List<String> feedTweets = admin.getNewsFeedFromID(userID);
        DefaultListModel<String> listModel = new DefaultListModel<>();
        for (String tweet : feedTweets) {
            listModel.addElement(tweet + "\n");
        }
        newsFeedListView.setModel(listModel);
    }
    
    public void updateFeed(String tweet) {
        DefaultListModel<String> listModel = (DefaultListModel<String>)newsFeedListView.getModel();
        if (!listModel.contains(tweet)) {
            listModel.addElement(tweet);
            String time = new Timestamp(admin.getLastUpdateTime(userID)).toString();
            lastUpdateDate.setText("Last Update: " + time);
        }
    }

    public void updateFollowerFeeds(String tweet) {
        List<String> followerIDs = admin.getFollowerIDs(userID);
        for (String follower : followerIDs) {
            admin.getUserViewFromID(follower).updateFeed(tweet);
        }
    }

    public void populateFollowingList() {
        List<String> followingList = admin.getFollowingIDs(userID);
        DefaultListModel<String> listModel = new DefaultListModel<>();
        for (String followedUser : followingList) {
            listModel.addElement(followedUser +"\n");
        }
        followingListView.setModel(listModel);
    }
    
    public void updateFollowingList(String followedUser) {
        DefaultListModel<String> listModel = (DefaultListModel<String>)followingListView.getModel();
        if (!listModel.contains(followedUser)) {
            listModel.addElement(followedUser);
        }
    }

